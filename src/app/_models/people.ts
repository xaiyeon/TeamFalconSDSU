export class People {
    public Name: string;
    public Photo: string;
    public Major: string;
    public Description: string;
    public Style: string;

    constructor(name: string, photo: string, major: string, description: string, style: string) {
        this.Name = name;
        this.Photo = photo;
        this.Major = major;
        this.Description = description;
        this.Style = style;

    }
}
